package com.domain;

public class Vuser {
    private int Vid;
    private String User;
    private String Tel;

    public void setVid(int vid) {
        Vid = vid;
    }

    public void setUser(String user) {
        User = user;
    }

    public void setTel(String tel) {
        Tel = tel;
    }

    public int getVid() {
        return Vid;
    }

    public String getUser() {
        return User;
    }

    public String getTel() {
        return Tel;
    }
}
